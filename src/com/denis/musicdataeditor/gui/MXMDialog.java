    private void b_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_saveActionPerformed
        ID3v2 tags = new ID3v23Tag();
        tags.setTitle(References.toTitleCase(t_titolo.getText()));
        tags.setArtist(References.toTitleCase(t_artista.getText()));
        tags.setAlbumArtist(References.toTitleCase(t_artista.getText()));
        tags.setAlbum(References.toTitleCase(t_album.getText()));
        tags.setYear(References.toTitleCase(t_anno.getText()));
        tags.setTrack(References.toTitleCase(t_numero.getText()));
        if (!cb_genere.getSelectedItem().toString().equals("Unknown")) {
            tags.setGenreDescription((String)cb_genere.getSelectedItem());
        }
        if (l_cover.getIcon() != null) {
            tags.setAlbumImage(CoverArt.iconToByteArray(l_cover.getIcon()), "jpg");
        }
        song.setTrack_tags(tags);
        song.save();

        this.dispose();

    }//GEN-LAST:event_b_saveActionPerformed

    private void b_translTitleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_translTitleActionPerformed
        if (tracks == null) return;
        MXMTrack track = tracks.get(pos);
        if (track == null) return;

        if (track.getTitleTranslations() == null ||
                track.getTitleTranslations().size() == 0) // la seconda parte del if non è necessaria, ma la metto per sicurezza
        {
            JOptionPane.showMessageDialog(this, "No translated title name found.");
            return;
        }

        Object[] translations = References.normalizeTitleTranslation(track);
        String result = (String)JOptionPane.showInputDialog(
                this,
                "These translations were found",
                "Title translations",
                JOptionPane.PLAIN_MESSAGE,
                null,
                translations, translations[0]);

        if (result != null && result.length() > 0) t_artista.setText(result);
    }//GEN-LAST:event_b_translTitleActionPerformed

