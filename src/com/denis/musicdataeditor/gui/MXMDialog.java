    private void b_albCoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_albCoverActionPerformed
        if (tracks == null) return;
        MXMTrack track = tracks.get(pos);
        if (track.getAlbumCover350x350() == null) {
            JOptionPane.showConfirmDialog(this, "No album cover found", "Album Coverart 350x350px", JOptionPane.PLAIN_MESSAGE);
            return;
        }

        ImageIcon im = new ImageIcon(track.getAlbumCover350x350());
        int result = JOptionPane.showConfirmDialog(
                this,
                "Do you really want to use this coverart instead of the tracks?",
                "Album Coverart 350x350px",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.PLAIN_MESSAGE,
                im
        );
        if (result == 0) l_cover.setIcon(im);
    }//GEN-LAST:event_b_albCoverActionPerformed

