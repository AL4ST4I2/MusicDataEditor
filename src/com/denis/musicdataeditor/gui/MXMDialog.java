    private void guiController(MXMTrack t)
    {
        if (t != null) {
            jLabel2.setText("Track found index: " + (pos + 1) + " of " + tracks.size() + " id: " + String
                    .valueOf(t.getTrackID()));

            t_titolo.setText(t.getTitle());
            t_artista.setText(t.getArtist());
            t_album.setText(t.getAlbum());
            t_anno.setText(t.getReleaseDate());
            t_numero.setText(String.valueOf(t.getTrackNumber()));
            cb_genere.setModel(new DefaultComboBoxModel<String>(References.normalizeTrackGenres(t)));


            if (t.getCoverart350x350() != null) {
                l_cover.setIcon(new ImageIcon(t.getCoverart350x350()));
            } else if (t.getCoverart100x100() != null) {
                // questo blocco else if è stato aggiunto in seguito, è da controllare, ma basicamente serve
                // a far si che trovi una cover 100px qualora non abbia una 350px
                l_cover.setIcon(new ImageIcon(t.getCoverart100x100()));
            } else {
                l_cover.setIcon(null);
                l_cover.setText("No Track Cover Found :(");
            }
        } else {
            jLabel2.setText("No track Seleceted/Found");
        }

    }

    private String[] setGeneriArray()
    {
        String[] unk = new String[] {"unknown"};
        if (tracks.get(pos).getGenres().size() ==  0) return unk;
        String[] rit = new String[tracks.get(pos).getGenres().size()];
        for (int i = 0; i < rit.length; i++) {
            rit[i] = tracks.get(pos).getGenres().get(i).getGenreName();
        }
        return rit;
    }

